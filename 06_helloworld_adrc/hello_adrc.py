import numpy as np


# =========================
# 1. 基本参数
# =========================
# 这里演示 ADRC（主动扰动抑制控制）的核心想法：
# - 不试图把所有扰动都精确建模
# - 而是把“未知项”统一看成总扰动
# - 再用扩张状态观测器 ESO 实时估计并补偿
dt = 0.005
T = 5.0
b0 = 9.81
disturbance = 1.5

# 控制器参数：这里用最基础的 PD 结构。
kp = 45.0
kd = 12.0

# ESO 参数：带宽越高，观测更快，但也更容易放大噪声。
beta1 = 120.0
beta2 = 2200.0
beta3 = 8000.0


# 真实系统状态。
x = 0.05
v = 0.0

# ESO 状态：
# - z1 估计位置 x
# - z2 估计速度 v
# - z3 估计总扰动（外扰 + 未建模项）
z1 = 0.0
z2 = 0.0
z3 = 0.0


steps = int(T / dt)
for i in range(steps):
    # 传感器输出，这里假设能直接测到位置。
    y = x

    # ---------- 扩张状态观测器 ESO ----------
    # e 是观测误差：估计位置减去测量位置。
    e = z1 - y

    # 下面三条就是 ESO 的离散更新。
    # 它一边估计系统状态，一边把“模型没写准的部分”收进 z3。
    z1 += dt * (z2 - beta1 * e)
    z2 += dt * (z3 - beta2 * e)

    # ---------- 控制律 ----------
    # 先基于估计状态做一个普通 PD 控制 u0。
    u0 = -kp * z1 - kd * z2

    # 再用观测到的总扰动 z3 进行补偿。
    # 除以 b0 是因为系统写成 x_ddot = b0 * u + total_disturbance。
    u = (u0 - z3) / b0
    u = float(np.clip(u, -0.25, 0.25))

    # ESO 对总扰动的更新放在最后一项。
    z3 += dt * (-beta3 * e)

    # ---------- 真实系统 ----------
    # 真实系统中有一个恒定外扰 disturbance。
    a = b0 * u + disturbance
    v += a * dt
    x += v * dt

    # 打印真值与 ESO 内部状态，观察观测与补偿过程。
    if i % 20 == 0:
        print(
            f"time={i*dt:.2f}s | x={x:.4f} | v={v:.4f} | "
            f"z1={z1:.4f} | z2={z2:.4f} | z3={z3:.4f} | u={u:.4f}"
        )
